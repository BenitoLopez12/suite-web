                                <p>• Eliminación: Suprimir la fuente del riesgo.</p>
                                <p>• Cambio de probabilidad: Implementar un control que disminuya los incidentes de seguridad de la información.</p>
                                <p>• Cambio en las consecuencias: Implementar un control que disminuya el impacto si llegase a ocurrir un incidente.</p>
                                <p>• Transferencia del riesgo: Externalizar una actividad o proceso a un tercero que tenga mayor capacidad para administrar el riesgo.</p>
                                <p>• Aceptar el riesgo: Si la organización no tiene disponible un método de tratamiento de riesgos real, o si el costo del método de tratamiento de riesgos se considera mayor que el costo de impacto, la organización puede tomar la decisión de aceptar el riesgo, lo cual será aprobado por la gerencia.</p>
